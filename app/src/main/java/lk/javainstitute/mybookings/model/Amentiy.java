package lk.javainstitute.mybookings.model;

public class Amentiy {
    private int icon;
    private String name;

    public Amentiy(int icon, String name) {
        this.icon = icon;
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public String getName() {
        return name;
    }
}
